/**
 * Search the static crypto list.
 * Priority: exact symbol match → symbol prefix → name prefix → name substring.
 * Case-insensitive. Returns up to `limit` results.
 */
export function searchCrypto(query: string, limit = 6): CryptoCoin[] {
  const q = query.trim().toUpperCase();
  if (!q) return [];

  const qLower = q.toLowerCase();

  const exactSymbol: CryptoCoin[] = [];
  const prefixSymbol: CryptoCoin[] = [];
  const prefixName: CryptoCoin[] = [];
  const substringName: CryptoCoin[] = [];

  for (const coin of CRYPTO_COINS) {
    const symUpper = coin.symbol.toUpperCase();
    const nameLower = coin.name.toLowerCase();

    if (symUpper === q) {
      exactSymbol.push(coin);
    } else if (symUpper.startsWith(q)) {
      prefixSymbol.push(coin);
    } else if (nameLower.startsWith(qLower)) {
      prefixName.push(coin);
    } else if (nameLower.includes(qLower)) {
      substringName.push(coin);
    }
  }

  return [...exactSymbol, ...prefixSymbol, ...prefixName, ...substringName].slice(0, limit);
}
